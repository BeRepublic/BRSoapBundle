<?php

namespace Cms\ModelBundle\Entity;

use Doctrine\ORM\Mapping as ORM;

/**
 * LogCampaign
 *
 * @ORM\Table(name="sample")
 * @ORM\Entity
 * @ORM\Entity(repositoryClass="Cms\ModelBundle\Repository\SampleRepository")
 */
class Sample
{
    /**
     * @var integer
     *
     * @ORM\Column(name="id", type="integer", nullable=false)
     * @ORM\Id
     * @ORM\GeneratedValue(strategy="IDENTITY")
     */
    private $id;

    /**
     * @var string
     *
     * @ORM\Column(name="name", type="text", nullable=false)
     */
    private $name;



    /**
     * Get id
     *
     * @return integer 
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * Set name
     *
     * @param string $name
     * @return Sample
     */
    public function setName($name)
    {
        $this->name = $name;
    
        return $this;
    }

    /**
     * Get name
     *
     * @return string 
     */
    public function getName()
    {
        return $this->name;
    }
}